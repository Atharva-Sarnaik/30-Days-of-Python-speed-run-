# if-else statement

age=int(input("Enter Your Age:"))
print("Your age is",age)
if age>=25:
    print("You can drink!")
    print('YES!') 
else:
    print("You cannot drink!")
    print("NO!")#Space after the else block means "indentation" in python which means that part is included in else block
print("YAY!") # This will be printed regardless of if-else

