# For loop in string
car="Lamborghini"
for i in car:
    print(i)

# For loop in list
a=["john","matt","lily","becky"]
for j in a:
    print(j)
    for k in j:
        print(k)

# Range function in for loop
for i in range(5):
    print(i)
for i in range(5):
    print(i+1)

for j in range(1,12,2):
    print(j)

